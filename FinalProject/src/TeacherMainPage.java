        public void loadQuestions() {
        // Clear existing rows
        model.setRowCount(0);

        List<Question> questions = operations.getQuestions();
        for (Question question : questions) {
            model.addRow(new Object[]{
                question.getQuestionId(),
                question.getStudentId(),
                question.getStudentUsername(),
                question.getCourseName(),
                question.getImagePath()
            });
        }
    }
        
        
        
 
