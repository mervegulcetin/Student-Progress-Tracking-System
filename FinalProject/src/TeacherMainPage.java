    private void uploadsol_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_uploadsol_buttonActionPerformed

        int selectedRow = table.getSelectedRow();
        if (selectedRow != -1) {
            int questionId = (int) table.getValueAt(selectedRow, 0);
            JFileChooser fileChooser = new JFileChooser();
            fileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
            fileChooser.setFileFilter(new FileNameExtensionFilter("Image files", "jpg", "jpeg", "png", "gif"));
            
            int returnValue = fileChooser.showOpenDialog(this);
            if (returnValue == JFileChooser.APPROVE_OPTION) {
                String solutionPath = fileChooser.getSelectedFile().getAbsolutePath();
                operations.uploadSolution(questionId, solutionPath);
                JOptionPane.showMessageDialog(this, "Solution uploaded successfully!");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please select a question to upload the answer.");
        }
        
    }//GEN-LAST:event_uploadsol_buttonActionPerformed

